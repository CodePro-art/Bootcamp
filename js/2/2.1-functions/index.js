function describeCountry(country='Finland',population='6 million',capitalCity='Helsinki'){
  return `${country} has ${population} people and its capital city is ${capitalCity}`;
}

console.log(describeCountry('Germany', '3.769 million', 'Berlin'));
console.log(describeCountry('France', '2.148 million', 'Paris'));
console.log(describeCountry('China', '21.54 million', 'Beijin'));